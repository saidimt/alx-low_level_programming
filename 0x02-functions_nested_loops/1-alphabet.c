#include "main.h"
/**
 *
 *  * print_alphabet - Entry point
 *
 *   * Description: a function that prints the alphabet,
 *
 *    * in lowecase followed by a new line
 *
 *     * Return: void
 *
 *      */
void print_alphabet(void)
{
char c;
for (c = 'a'; c <= 'z'; c++)
_putchar(c);
_putchar('\n');
}
